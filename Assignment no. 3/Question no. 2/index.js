/*

There are three types of errors. Syntex error, Runtime error and logical error.



Syntex error:

Syntex error is occour when programmer didn't write program according to the language's syntex rules.




Runtime error:

This error occur when code attemps to perform an operation that is not allowed.
For example: Divived by 0.



Logical error:

Logical error occur when code doesn't produce expected resutl. Logical error occur due to the wrong coding of programmer.


*/






